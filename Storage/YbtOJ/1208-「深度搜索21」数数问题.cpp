bool check(vector<int> &num) {
    int a = 0, b = 0;
    for (int i = 0; i < (int)num.size(); ++i) {
        if (g[i])
            b += num[i];
        else
            a += num[i];
    }
    return a == b;
}

bool calc(int dep, vector<int> &num) {
    if (dep == (int)num.size())
        return check(num);
    bool res = calc(dep + 1, num);
    if (res)
        return true;
    g[dep] = 1;
    res = calc(dep + 1, num);
    g[dep] = 0;
    return res;
}

bool solve(int x) {
    vector<int> num;
    while (x) {
        num.push_back(x % 10);
        x /= 10;
    }
    memset(g, 0, sizeof(g));
    return calc(0, num);
}

int solve2(int x) {
    int id = x / bsize;
    int ans = table[id];
    for (int i = id * bsize + 1; i <= x; ++i)
        ans += solve(i);
    return ans;
}

